#Adding New Key-Value Pairs.
#
#
#Creating a dictionary
Fav_Colors = {"Josh": 'Purple',
              'Belle': 'Red',
              'Rachel': 'Blue'
              }
#Printing the list
print(Fav_Colors)
#
#
#Adding the values to the dictionary
Fav_Colors['Brian'] = 'Black'
Fav_Colors['Christina'] = 'Blue'
#
#
#Printing new dictionary with empty items.
print(Fav_Colors)

#Printing the Pair value associate with Brian.
dad = Fav_Colors['Brian']
#
#Using the pair value in the printed statement.
print(f"My dad fav color is {dad}")